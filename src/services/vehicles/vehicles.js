class Vehicle {
    constructor(id, type, entryTime, exitTime) {
      this.id = id; // Unique identifier for the vehicle
      this.type = type; // Type of vehicle (e.g., motorcycle, car, bus)
      this.entryTime = entryTime; // Time when the vehicle entered the parking lot
      this.exitTime = exitTime; // Time when the vehicle exited the parking lot
    }
  }